package javacorepart1.conditions;

public class ExampleSwitchCase {

	public static void main(String[] args) {
		int j= 2;
		
		switch(j){
			case 0:
				break;
				
			case 1:
				System.out.println("Value is " + j);
				break;
			case 2:
				System.out.println("Value is " + j);

				break;
			case 3:
				System.out.println("Value is " + j);
				break;
			default:
				System.out.println("no value");
		}
	}
}
