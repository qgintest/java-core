package javacorepart1.conditions;

public class ExampleIfThenElse {

	public static void main(String[] args) {
		
		
		int x = 20; //semicolon is put for complete statements
		int y = 20;
		
		if(x<y){
			System.out.println("less than");
		}else if(x == y){
			System.out.println("equal to");
		}else{
			System.out.println("greater than");
		}
		
		//relational operators include <, >, ==, <=, >=. RO: operators that compare between two values

	}

}
